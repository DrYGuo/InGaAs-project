function F200=F200(occ,F200_struct,s)
%This function follows the paper

x=occ;
faR_struct=F200_struct.faR_struct;
vcratio=F200_struct.vcratio;

%	Ga for GaAs  	As for GaAs	   In for InAs	  As for InAs
% p1	0.0002619	0.0003178	   0.0002278	    0.0001902
% p2	-0.001009	-0.001547	   -0.001372	     -0.001411
% p3	0.03205	     0.03471	    0.04348	           0.03443
% p4	0.5383	     0.5907	        0.8311	           0.5830

p1=[0.0002619	0.0003178	   0.0002278	    0.0001902];
p2=[-0.001009	-0.001547	   -0.001372	     -0.001411];
p3=[0.03205	     0.03471	    0.04348	           0.03443];
p4=[0.5383	     0.5907	        0.8311	           0.5830];

%      Ga for GaAs  As for GaAs	 In for InAs	As for InAs
% dp1	  0.0095	0.0078	      0.0093	     0.0086
% dp2	  0.0051	0.0223	      0.0053	     0.0254
% dp3	 -0.0145	-0.0435	     -0.0145	     -0.0222
% dp4	  1.0000	1.0000	      1.0000	     0.9883

dp1=[0.0095 	0.0078	      0.0093	     0.0086];
dp2=[0.0051	   0.0223	      0.0053	     0.0254];
dp3=[-0.0145   -0.0435       -0.0145         -0.0222];
dp4=[1.0000	   1.0000	      1.0000	     0.9883];

f1=p1(1)*x^3+p2(1)*x^2+p3(1)*x+p4(1); %Ga in GaAs
f2=p1(2)*x^3+p2(2)*x^2+p3(2)*x+p4(2); %As in GaAs
f3=p1(3)*x^3+p2(3)*x^2+p3(3)*x+p4(3); %As in InAs
f4=p1(4)*x^3+p2(4)*x^2+p3(4)*x+p4(4); %As in InAs

d1=dp1(1)*x^3+dp2(1)*x^2+dp3(1)*x+dp4(1); %Ga in GaAs
d2=dp1(2)*x^3+dp2(2)*x^2+dp3(2)*x+dp4(2); %As in GaAs
d3=dp1(3)*x^3+dp2(3)*x^2+dp3(3)*x+dp4(3); %In in InAs
d4=dp1(4)*x^3+dp2(4)*x^2+dp3(4)*x+dp4(4); %As in InAs

M1=8*pi^2*0.00865892;  %8*pi^2*0.0084; %8*pi^2*0.00775;                %
M2=8*pi^2*0.0074442;                   %8*pi^2*0.0245;                  %8*pi^2*0.01515;                %%
M3=8*pi^2*0.0127158;%8*pi^2*0.0139; %8*pi^2*0.010618;    %   8*pi^2*0.0139;        %                %
M4=8*pi^2*0.0108519;%8*pi^2*0.0245;                %8*pi^2*0.01515;             %
% M1=0;
% M2=0;
% M3=0;
% M4=0;
% F200=4*((1-x)*(d1*f1-d2*f2)+x*(d3*f3-d4*f4));
F200=4*((1-x)*(d1*f1*exp(-M1*s^2)*(1+1i*faR(s,M1,31,faR_struct)/(100*vcratio))-d2*f2*exp(-M2*s^2)*(1+1i*faR(s,M2,33,faR_struct)/(100*vcratio)))+x*(d3*f3*exp(-M3*s^2)*(1+1i*faR(s,M3,49,faR_struct)/(100*vcratio))-d4*f4*exp(-M4*s^2)*(1+1i*faR(s,M4,33,faR_struct)/(100*vcratio))));


end